package org.lvyouzaike.freemarker;

public class Entity {
	private String title;             //title of a zaike entity
	private String summary;           //summary of a zaike entity
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
}
